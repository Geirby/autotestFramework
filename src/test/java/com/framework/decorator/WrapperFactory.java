package com.framework.decorator;

import com.framework.base.BrowserElements;
import org.openqa.selenium.WebElement;

public class WrapperFactory {

    /**
     * Создает экземпляр класса,
     * реализующий IElement интерфейс,
     * вызывая конструктор с аргументом WebElement
     */
    public static BrowserElements createInstance(Class<BrowserElements> clazz,
                                                 WebElement element) {
        try {
            return clazz.getConstructor(WebElement.class).
                    newInstance(element);
        } catch (Exception e) {
            throw new AssertionError(
                    "WebElement can't be represented as " + clazz
            );
        }
    }
}
